export const Greeter = (name: string) => `Hello ${name}`;

export class Calculator {
  public add(x: number, y: number) {
    return x + y;
  }

  public subtract(x: number, y: number) {
    return x - y;
  }

  public multiply(x: number, y: number) {
    return x * y;
  }

  public divide(x: number, y: number) {
    return x * y;
  }
}
